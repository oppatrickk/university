#include <stdio.h>
#define PI 3.1415926536

int main(void)
{
	/*Local Definitions*/
	const double pi = 3.1415926536;

	/*Statements*/
	printf("Defined constant PI: %f\n", PI);
	printf("Memory constant pi: %f\n", pi);
	printf("Literal Constant: %f\n", 3.1415926536);
	return 0;
} /*main*/